                  final day = now.subtract(Duration(days: 10 - i));
                  final isSelected = _dateKey(day) == _dateKey(_selectedDate);
                  final isToday = _dateKey(day) == _dateKey(now);
                  final hasPlans = _hasPlans(day);

                  return GestureDetector(
                    onTap: () => setState(() => _selectedDate = day),
                    child: Container(
                      width: 52,
                      margin: const EdgeInsets.symmetric(horizontal: 4),
                      decoration: BoxDecoration(
                        color: isSelected
                            ? accent
                            : isToday
                                ? const Color(0xFFFFD700)
                                : Theme.of(context).colorScheme.surface,
                        borderRadius: BorderRadius.circular(16),
                      ),
                      child: Column(
                        mainAxisAlignment: MainAxisAlignment.center,
                        children: [
                          Text(
                            ['Пн','Вт','Ср','Чт','Пт','Сб','Вс'][day.weekday - 1],
                            style: TextStyle(
                              fontSize: 11,
                              color: isSelected || isToday
                                  ? (isSelected ? Colors.white : Colors.black)
                                  : textColor.withOpacity(0.5),
                            ),
                          ),
                          const SizedBox(height: 4),
                          Text(
                            '${day.day}',
                            style: TextStyle(
                              fontSize: 18,
                              fontWeight: FontWeight.w800,
                              color: isSelected
                                  ? Colors.white
                                  : isToday
                                      ? Colors.black
                                      : textColor,
                            ),
                          ),
                          if (hasPlans)
                            Container(
                              width: 4,
                              height: 4,
                              margin: const EdgeInsets.only(top: 3),
                              decoration: BoxDecoration(
                                color: isSelected ? Colors.white : accent,
                                shape: BoxShape.circle,
                              ),
                            ),
                        ],
                      ),
                    ),
                  );
                },
              ),
            ),

            const SizedBox(height: 16),

            // ── Планы выбранного дня ──
            Expanded(
              child: _selectedPlans.isEmpty
                  ? Center(
                      child: Column(
                        mainAxisAlignment: MainAxisAlignment.center,
                        children: [
                          Text('📋', style: const TextStyle(fontSize: 48)),
                          const SizedBox(height: 16),
                          Text('Нет планов на этот день',
                              style: TextStyle(fontSize: 16, color: textColor.withOpacity(0.5))),
                          const SizedBox(height: 8),
                          Text('Нажми + чтобы добавить',
                              style: TextStyle(fontSize: 13, color: textColor.withOpacity(0.3))),
                        ],
                      ),
                    )
                  : ReorderableListView.builder(
                      padding: const EdgeInsets.symmetric(horizontal: 24),
                      itemCount: _selectedPlans.length,
                      onReorder: (oldIndex, newIndex) {
                        if (newIndex > oldIndex) newIndex--;
                        final key = _dateKey(_selectedDate);
                        final plans = List<Map<String, dynamic>>.from(_allPlans[key] ?? []);
                        final item = plans.removeAt(oldIndex);
                        plans.insert(newIndex, item);
                        setState(() => _allPlans[key] = plans);
                        widget.onSave(key, plans);
                      },
                      itemBuilder: (_, i) {
                        final plan = _selectedPlans[i];
                        final key = _dateKey(_selectedDate);
                        final done = plan['done'] as bool;

                        return Material(
                          key: ValueKey(_selectedPlans[i]['id']),
                          color: Colors.transparent,
                          borderRadius: BorderRadius.circular(16),
                          child: Container(
                          margin: const EdgeInsets.only(bottom: 12),
                          padding: const EdgeInsets.all(16),
                          decoration: BoxDecoration(
                            color: Theme.of(context).colorScheme.surface,
                            borderRadius: BorderRadius.circular(16),
                            border: done
                                ? Border.all(color: Colors.green.withOpacity(0.3))
                                : null,
                          ),
                          child: Row(
                            children: [
                              GestureDetector(
                                onTap: () => _toggleDone(key, i),
                                child: Container(
                                  width: 24,
                                  height: 24,
                                  decoration: BoxDecoration(
                                    color: done ? Colors.green : Colors.transparent,
                                    border: Border.all(
                                      color: done ? Colors.green : textColor.withOpacity(0.3),
                                      width: 2,
                                    ),
                                    shape: BoxShape.circle,
                                  ),
                                  child: done
                                      ? const Icon(Icons.check, size: 14, color: Colors.white)
                                      : null,
                                ),
                              ),
                              const SizedBox(width: 12),
                              Expanded(
                                child: Column(
                                  crossAxisAlignment: CrossAxisAlignment.start,
                                  children: [
                                    Text(
                                      plan['text'] as String,
                                      style: TextStyle(
                                        fontSize: 15,
                                        fontWeight: FontWeight.w600,
                                        color: done
                                            ? textColor.withOpacity(0.4)
                                            : textColor,
                                        decoration: done
                                            ? TextDecoration.lineThrough
                                            : null,
                                      ),
                                    ),
                                    if (plan['time'] != null) ...[
                                      const SizedBox(height: 4),
                                      Row(
                                        children: [
                                          Icon(Icons.access_time,
                                              size: 12,
                                              color: accent.withOpacity(0.7)),
                                          const SizedBox(width: 4),
                                          Text(plan['time'] as String,
                                              style: TextStyle(
                                                  fontSize: 12,
                                                  color: accent.withOpacity(0.7),
                                                  fontWeight: FontWeight.w600)),
                                        ],
                                      ),
                                    ],
                                  ],
                                ),
                              ),
                              Row(
                                mainAxisSize: MainAxisSize.min,
                                children: [
                                  if (plan['time'] != null) ...[
                                    GestureDetector(
                                      onTap: () async {
                                        final isRepeating = plan['repeating'] as bool? ?? false;
                                        final plans = List<Map<String, dynamic>>.from(_allPlans[key] ?? []);
                                        plans[i] = {...plans[i], 'repeating': !isRepeating};
                                        setState(() => _allPlans[key] = plans);
                                        widget.onSave(key, plans);
                                        if (!isRepeating) {
                                          final parts = (plan['time'] as String).split(':');
                                          final when = DateTime(
                                            _selectedDate.year, _selectedDate.month, _selectedDate.day,
                                            int.parse(parts[0]), int.parse(parts[1]));
                                          for (int r = 0; r < 20; r++) {
                                            final fireTime = when.add(Duration(minutes: r * 3));
                                            if (fireTime.isAfter(DateTime.now())) {
                                              await notif.notifications.zonedSchedule(
                                                id: (plan['id'] as String).hashCode + 1000 + r,
                                                title: '📋 Modo — не забудь!',
                                                body: plan['text'] as String,
                                                scheduledDate: tz.TZDateTime.from(fireTime, tz.local),
                                                notificationDetails: const NotificationDetails(
                                                  android: AndroidNotificationDetails(
                                                    'repeating_channel', 'Настойчивые напоминания',
                                                    channelDescription: 'Повторяющиеся уведомления',
                                                    importance: Importance.high,
                                                    priority: Priority.high,
                                                  ),
                                                ),
                                                androidScheduleMode: AndroidScheduleMode.exactAllowWhileIdle,
                                              );
                                            }
                                          }
                                        } else {
                                          for (int r = 0; r < 20; r++) {
                                            await notif.notifications.cancel(id: (plan['id'] as String).hashCode + 1000 + r);
                                          }
                                        }
                                      },
                                      child: Container(
                                        margin: const EdgeInsets.only(right: 8),
                                        padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
                                        decoration: BoxDecoration(
                                          color: (plan['repeating'] as bool? ?? false)
                                              ? accent.withOpacity(0.15)
                                              : textColor.withOpacity(0.06),
                                          borderRadius: BorderRadius.circular(8),
                                        ),
                                        child: Row(
                                          mainAxisSize: MainAxisSize.min,
                                          children: [
                                            Icon(Icons.notifications_active_outlined,
                                                size: 14,
                                                color: (plan['repeating'] as bool? ?? false)
                                                    ? accent
                                                    : textColor.withOpacity(0.3)),
                                            const SizedBox(width: 4),
                                            Text('×3м',
                                                style: TextStyle(
                                                    fontSize: 11,
                                                    color: (plan['repeating'] as bool? ?? false)
                                                        ? accent
                                                        : textColor.withOpacity(0.3),
                                                    fontWeight: FontWeight.w600)),
                                          ],
                                        ),
                                      ),
                                    ),
                                  ],
                                  GestureDetector(
                                    onTap: () => _deletePlan(key, i),
                                    child: Icon(Icons.delete_outline,
                                        size: 20,
                                        color: textColor.withOpacity(0.3)),
                                  ),
                                ],
                              ),
                            ],
                          ),
                          ),
                        );
                      },
                    ),
                    
            ),
          ],
        ),
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _addPlan,
        backgroundColor: accent,
        foregroundColor: Colors.white,
        elevation: 0,
        child: const Icon(Icons.add),
      ),
    );
  }
