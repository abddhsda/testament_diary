class MindfulDiaryApp extends StatefulWidget {
  const MindfulDiaryApp({super.key});

  @override
  State<MindfulDiaryApp> createState() => _MindfulDiaryAppState();
}

class _MindfulDiaryAppState extends State<MindfulDiaryApp> {
ThemeMode _themeMode = ThemeMode.light;
  Color _accent = const Color(0xFFE8927C);
  bool _isPremium = false;
  
  bool _settingsLoaded = false;

  @override
  void initState() {
    super.initState();
    _loadSettings();
  }

  Future<void> _loadSettings() async {
    final prefs = await SharedPreferences.getInstance();
    final isDark = prefs.getBool('isDark') ?? false;
    final accentIndex = prefs.getInt('accentIndex') ?? 0;
    const accents = [
      Color(0xFFE8927C),
      Color(0xFF5B8CDB),
      Color(0xFF9B59B6),
      Color(0xFF2ECC71),
    ];
    final isPremium = prefs.getBool('isPremium') ?? false;
    setState(() {
      _themeMode = isDark ? ThemeMode.dark : ThemeMode.light;
      _accent = accents[accentIndex];
      _isPremium = isPremium;
      _settingsLoaded = true;
    });
  }

  void _setTheme(ThemeMode mode) async {
    setState(() => _themeMode = mode);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setBool('isDark', mode == ThemeMode.dark);
  }

  void _setAccent(Color color) async {
    setState(() => _accent = color);
    const accents = [
      Color(0xFFE8927C),
      Color(0xFF5B8CDB),
      Color(0xFF9B59B6),
      Color(0xFF2ECC71),
    ];
    final prefs = await SharedPreferences.getInstance();
    await prefs.setInt('accentIndex', accents.indexOf(color));
  }
    void _setIsPremium(bool value) async {
    setState(() => _isPremium = value);
    final prefs = await SharedPreferences.getInstance();
    await prefs.setBool('isPremium', value);
  }



  @override
  Widget build(BuildContext context) {
    if (!_settingsLoaded) return const MaterialApp(home: Scaffold(body: SizedBox()));
    
          return AppSettings(
      themeMode: _themeMode,
      accent: _accent,
      isPremium: _isPremium,
      setTheme: _setTheme,
      setAccent: _setAccent,
      setIsPremium: _setIsPremium,
      
      child: MaterialApp(
        title: 'Modo',
        debugShowCheckedModeBanner: false,
        themeMode: _themeMode,
        theme: _buildTheme(_accent, Brightness.light),
        darkTheme: _buildTheme(_accent, Brightness.dark),
        localizationsDelegates: const [
          GlobalMaterialLocalizations.delegate,
          GlobalWidgetsLocalizations.delegate,
        ],
        supportedLocales: const [Locale('ru')],
        home: const HomeScreen(),
      ),
    );
  }

  ThemeData _buildTheme(Color accent, Brightness brightness) {
    final isDark = brightness == Brightness.dark;
    final bg = isDark
        ? Color.alphaBlend(accent.withOpacity(0.05), const Color(0xFF121212))
        : Color.alphaBlend(accent.withOpacity(0.04), const Color(0xFFFFFBF8));
    final surfaceColor = isDark
        ? Color.alphaBlend(accent.withOpacity(0.08), const Color(0xFF1E1E1E))
        : Color.alphaBlend(accent.withOpacity(0.08), const Color(0xFFEEEEEE));
    return ThemeData(
      brightness: brightness,
      colorScheme: ColorScheme.fromSeed(seedColor: accent, brightness: brightness).copyWith(
        surface: surfaceColor,
      ),
      scaffoldBackgroundColor: bg,
      useMaterial3: true,
    );
  }
}

class AppSettings extends InheritedWidget {
  final ThemeMode themeMode;
  final Color accent;
  final bool isPremium;
  final void Function(ThemeMode) setTheme;
  final void Function(Color) setAccent;
  final void Function(bool) setIsPremium;

  const AppSettings({
    super.key,
    required this.themeMode,
    required this.accent,
    required this.isPremium,
    required this.setTheme,
    required this.setAccent,
    required this.setIsPremium,
    required super.child,
  });

  static AppSettings of(BuildContext context) =>
      context.dependOnInheritedWidgetOfExactType<AppSettings>()!;

  @override
  bool updateShouldNotify(AppSettings old) =>
      themeMode != old.themeMode || accent != old.accent || isPremium != old.isPremium;
}


